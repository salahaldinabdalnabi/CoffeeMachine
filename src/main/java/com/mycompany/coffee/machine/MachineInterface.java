    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int cups = Integer.parseInt(cupCap.getText());
        int watercap = Integer.parseInt(waterCap.getText());
        int water = Integer.parseInt(curWater.getText());
        int beancap = Integer.parseInt(coffeeCap.getText());
        int beans = Integer.parseInt(curCoffee.getText());
       
        if(clean.getText().equals("Yes"))
            machine = new CoffeeMachine(cups,watercap,water,beancap,beans,true); 
        else if(clean.getText().equals("No"))
            machine = new CoffeeMachine(cups,watercap,water,beancap,beans,false); 
        else 
            JOptionPane.showMessageDialog(null,"clean field can only be a Yes or No");      
         
        cupCap.setText("");
        waterCap.setText("");
        curWater.setText("");
        coffeeCap.setText("");
        curCoffee.setText("");
        clean.setText("");
        
        JOptionPane.showMessageDialog(null, "Machine Was Created");
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

