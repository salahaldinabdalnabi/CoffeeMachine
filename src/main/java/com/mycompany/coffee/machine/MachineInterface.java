    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Coffee bean; 
        if(EspressoBox.isSelected())
            bean = createEspresso(); 
        else 
            bean = createAmericano(); 
        
        try {
            machine.startMachine(bean);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Something Wrong with the machine");
        }
        
        ScreenArea.setText(machine.Report);
        String str = LoggerArea.getText() + " \n =================================== \n";
        LoggerArea.setText(str + "Report was Added to ../log.txt ");
    }//GEN-LAST:event_jButton2ActionPerformed

