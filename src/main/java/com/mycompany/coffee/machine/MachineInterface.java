    private Espresso createEspresso() {
            if(ArabicaBox.isSelected())
            {
                if(YesBox.isSelected())
                    return new Espresso("Arabica",true); 
                else
                    return new Espresso("Arabica",false);
            }
            if(RobustaBox.isSelected())
            {
                if(YesBox.isSelected())
                    return new Espresso("Robusta",true); 
                else
                    return new Espresso("Robusta",false);
            }
            if(BlendBox.isSelected())
            {
                if(YesBox.isSelected())
                    return new Espresso("Blend",true); 
                else
                    return new Espresso("Blend",false);
            }
        
        return null; 
    }
